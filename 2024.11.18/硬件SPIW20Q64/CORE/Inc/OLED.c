void OLED_WriteCommand(uint8_t command){
	IICruanjian_Start();
	IICruanjian_SendByte(0x78);
	IICruanjian_ReadAck();
	IICruanjian_SendByte(0x00);
	IICruanjian_ReadAck();
	IICruanjian_SendByte(command);
	IICruanjian_ReadAck();
	IICruanjian_Stop();
}

void OLED_WriteData(uint8_t arr[],uint8_t count){
	IICruanjian_Start();
	IICruanjian_SendByte(0x78);
	IICruanjian_ReadAck();
	IICruanjian_SendByte(0X40);
	IICruanjian_ReadAck();
	for(uint8_t i=0;i<count;i++){
		IICruanjian_SendByte(arr[i]);
		IICruanjian_ReadAck();
	}
	IICruanjian_Stop();
}

void OLED_Init(void){
	IICruanjian_Init();
	//发送命令 关闭显示
	OLED_WriteCommand(0XAE);
	//设置时钟分频比/振荡器频率
	OLED_WriteCommand(0XD5);	
	OLED_WriteCommand(0X80);
	
	//设置多路复用比率
	OLED_WriteCommand(0XA8);
	OLED_WriteCommand(0X3F);
	
	//设置显示偏移量
	OLED_WriteCommand(0XD3);
	OLED_WriteCommand(0X00);
	//设置显示起始线
	OLED_WriteCommand(0X40);
	//设置段重映射
	OLED_WriteCommand(0XA1);
	//设置COM输出扫描方向
	OLED_WriteCommand(0XC8);	
	//设置COM引脚硬件配置
	OLED_WriteCommand(0XDA);
	OLED_WriteCommand(0X12);
	//设置对比度设置
	OLED_WriteCommand(0X81);
	OLED_WriteCommand(0XCF);
	//设置预充期
	OLED_WriteCommand(0XD9);
	OLED_WriteCommand(0XF1);
	//设置VCOMH取消电平
	OLED_WriteCommand(0XDB);
	OLED_WriteCommand(0X30);
	//恢复到RAM显示
	OLED_WriteCommand(0XA4);
	//设置正向显示
	OLED_WriteCommand(0XA6);
	//设置充电泵
	OLED_WriteCommand(0X8D);
	OLED_WriteCommand(0X14);
	//设置显示打开
	OLED_WriteCommand(0XAF);

}

void OLED_SetLocation(uint8_t x,uint8_t y){
	//设置行
	OLED_WriteCommand(0xB0|x);
	//设置列
	OLED_WriteCommand(0x10|((y&0xf0)>>4));
	OLED_WriteCommand(0x00|(y&0x0f));

}

void OLED_Clear(void){
	for(uint8_t i=0;i<8;i++){
		OLED_SetLocation(i,0);
		OLED_WriteData(arr_clear[i],128);
	}
}

void OLED_UpData(void){
	for(uint8_t i=0;i<8;i++){
		OLED_SetLocation(i,0);
		OLED_WriteData(arr_main[i],128);
	}
}

void OLED_ClearArea(uint8_t x,uint8_t y,uint8_t length,uint8_t width){
	for(uint8_t i=y;i<=y+width;i++){
		for(uint8_t j=x;j<=x+length;j++){
			arr_main[i/8][j]&=~(0x01<<(i%8));
		}
	}
}

void OLED_ShowImage(uint8_t x,uint8_t y,uint8_t length,uint8_t width,const uint8_t arr[]){
	OLED_ClearArea(x,y,length,width);
	for(uint8_t j=0;j<(width-1)/8+1;j++){
		for(uint8_t i=0;i<length;i++){
		arr_main[y/8+j][x+i]|=arr[j*length+i]<<(y%8);
		arr_main[y/8+1+j][x+i]|=arr[j*length+i]>>(8-y%8);
		}
	}

}

